public class Exercise03 {

    public static void main(String[] args) {

        int count = 23;

        while (count > 16) {
            System.out.println(count);
            count--;
        }

        // 1. Write a loop to count backward from 23 to 17.

        // Expected Output
        // 23
        // 22
        // 21
        // 20
        // 19
        // 18
        // 17
    }
}
